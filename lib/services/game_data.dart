class GameData {
	final String id;
	final String name;
	final String author;
	final String version;
	final String bannerColor;
	final String bannerImage;

	GameData({
		required this.id,
		required this.name,
		required this.author,
		required this.version,
		required this.bannerColor,
		required this.bannerImage,
	});
}